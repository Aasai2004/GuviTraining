package day1;

public class SwitchStatement {
	public static void main(String[] str) {
		int to_find = 4;
	
		switch(to_find) {
		case 1 :
			System.out.println("AIDS");
			break;
		case 2 :
			System.out.println("IT");
			break;
		case 3 :
			System.out.println("BIO");
			break;
		case 4 :
			System.out.println("CSE");
			break;
		default :
			System.out.println("The value is not there");
		}
		
		}
	}


